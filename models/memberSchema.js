import mongoose from "mongoose";

const MembersSchema = new mongoose.Schema({
  name: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
  },
  phone: {
    type: String,
    require: true,
  },
  organisation: {
    type: String,
  },
  company: {
    type: String,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
  },
  updatedAt: {
    type: Date,
    default: Date.now(),
  },
});

const Members =
  mongoose.models.Members || mongoose.model("Members", MembersSchema);
export default Members;
